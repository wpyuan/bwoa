package com.bwjf.entity;

public class EmployeeDateVo {
	
	private String hireDateVo;			//入职日期
	private String birthdayVo;			//出生日期
	private String workDateVo;			//工作日期
	private String probationPeriodVo;		//试用期
	private String positivePhaseVo;		//转正日期
	private String graduationDateVo;		//毕业时间
	
	public String getHireDateVo() {
		return hireDateVo;
	}
	public void setHireDateVo(String hireDateVo) {
		this.hireDateVo = hireDateVo;
	}
	public String getBirthdayVo() {
		return birthdayVo;
	}
	public void setBirthdayVo(String birthdayVo) {
		this.birthdayVo = birthdayVo;
	}
	public String getWorkDateVo() {
		return workDateVo;
	}
	public void setWorkDateVo(String workDateVo) {
		this.workDateVo = workDateVo;
	}
	public String getProbationPeriodVo() {
		return probationPeriodVo;
	}
	public void setProbationPeriodVo(String probationPeriodVo) {
		this.probationPeriodVo = probationPeriodVo;
	}
	public String getPositivePhaseVo() {
		return positivePhaseVo;
	}
	public void setPositivePhaseVo(String positivePhaseVo) {
		this.positivePhaseVo = positivePhaseVo;
	}
	public String getGraduationDateVo() {
		return graduationDateVo;
	}
	public void setGraduationDateVo(String graduationDateVo) {
		this.graduationDateVo = graduationDateVo;
	}	
	
}
